-- Module 01 Authentication
-- PostgreSQL 17 / Neon compatible schema + sample seed data

BEGIN;

-- Drop child objects first for rerunnable script behavior in dev environments.
DROP TABLE IF EXISTS auth_tokens CASCADE;
DROP TABLE IF EXISTS login_activity_logs CASCADE;
DROP TABLE IF EXISTS admin_profiles CASCADE;
DROP TABLE IF EXISTS customer_profiles CASCADE;
DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS roles CASCADE;

CREATE TABLE roles (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code         VARCHAR(30)  NOT NULL UNIQUE,
    name         VARCHAR(60)  NOT NULL,
    description  VARCHAR(255),
    is_active    BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at   TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at   TIMESTAMP
);

CREATE TABLE users (
    id                    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_id               BIGINT       NOT NULL REFERENCES roles(id),
    email                 VARCHAR(150) NOT NULL UNIQUE,
    mobile_number         VARCHAR(20)  NOT NULL UNIQUE,
    password_hash         VARCHAR(255) NOT NULL,
    email_verified        BOOLEAN      NOT NULL DEFAULT FALSE,
    email_verified_at     TIMESTAMP,
    mobile_verified       BOOLEAN      NOT NULL DEFAULT FALSE,
    mobile_verified_at    TIMESTAMP,
    failed_login_attempts SMALLINT     NOT NULL DEFAULT 0,
    last_failed_login_at  TIMESTAMP,
    lock_until            TIMESTAMP,
    status                VARCHAR(30)  NOT NULL DEFAULT 'PENDING_VERIFICATION',
    disabled_reason       VARCHAR(255),
    disabled_by           BIGINT REFERENCES users(id),
    disabled_at           TIMESTAMP,
    last_login_at         TIMESTAMP,
    last_login_ip         VARCHAR(45),
    is_deleted            BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at            TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at            TIMESTAMP,
    CONSTRAINT chk_users_failed_login_attempts
        CHECK (failed_login_attempts >= 0 AND failed_login_attempts <= 3),
    CONSTRAINT chk_users_status
        CHECK (status IN ('PENDING_VERIFICATION', 'ACTIVE', 'LOCKED', 'DISABLED'))
);

CREATE TABLE customer_profiles (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id        BIGINT       NOT NULL UNIQUE REFERENCES users(id),
    full_name      VARCHAR(120) NOT NULL,
    date_of_birth  DATE,
    address_line_1 VARCHAR(150),
    address_line_2 VARCHAR(150),
    city           VARCHAR(80),
    state          VARCHAR(80),
    postal_code    VARCHAR(20),
    country        VARCHAR(80),
    kyc_status     VARCHAR(20)  NOT NULL DEFAULT 'PENDING',
    created_at     TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at     TIMESTAMP,
    CONSTRAINT chk_customer_kyc_status
        CHECK (kyc_status IN ('PENDING', 'APPROVED', 'REJECTED'))
);

CREATE TABLE admin_profiles (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id       BIGINT       NOT NULL UNIQUE REFERENCES users(id),
    full_name     VARCHAR(120) NOT NULL,
    employee_code VARCHAR(40)  NOT NULL UNIQUE,
    department    VARCHAR(80),
    designation   VARCHAR(80),
    created_at    TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP
);

CREATE TABLE login_activity_logs (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id          BIGINT REFERENCES users(id),
    login_identifier VARCHAR(150) NOT NULL,
    attempt_no       SMALLINT,
    result           VARCHAR(20)  NOT NULL,
    failure_reason   VARCHAR(120),
    ip_address       VARCHAR(45),
    user_agent       VARCHAR(500),
    device_id        VARCHAR(120),
    location_hint    VARCHAR(120),
    created_at       TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_login_result
        CHECK (result IN ('SUCCESS', 'FAILURE', 'BLOCKED', 'DISABLED'))
);

CREATE TABLE auth_tokens (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id       BIGINT       NOT NULL REFERENCES users(id),
    token_type    VARCHAR(30)  NOT NULL,
    channel       VARCHAR(20),
    token_hash    VARCHAR(255) NOT NULL UNIQUE,
    expires_at    TIMESTAMP    NOT NULL,
    consumed_at   TIMESTAMP,
    attempt_count SMALLINT     NOT NULL DEFAULT 0,
    max_attempts  SMALLINT     NOT NULL DEFAULT 3,
    is_used       BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at    TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_auth_token_type
        CHECK (token_type IN ('EMAIL_VERIFY', 'MOBILE_VERIFY', 'PASSWORD_RESET')),
    CONSTRAINT chk_auth_channel_values
        CHECK (channel IN ('EMAIL', 'MOBILE') OR channel IS NULL),
    CONSTRAINT chk_auth_token_channel_consistency
        CHECK (
            (token_type = 'PASSWORD_RESET' AND channel IS NULL)
            OR
            (token_type IN ('EMAIL_VERIFY', 'MOBILE_VERIFY') AND channel IS NOT NULL)
        ),
    CONSTRAINT chk_auth_attempt_limits
        CHECK (attempt_count >= 0 AND max_attempts >= 1)
);

-- Core indexes
CREATE INDEX idx_users_role_status ON users(role_id, status);
CREATE INDEX idx_users_lock_until ON users(lock_until);
CREATE INDEX idx_login_activity_user_created_at ON login_activity_logs(user_id, created_at DESC);
CREATE INDEX idx_login_activity_identifier_created_at ON login_activity_logs(login_identifier, created_at DESC);
CREATE INDEX idx_auth_tokens_user_type_expiry ON auth_tokens(user_id, token_type, expires_at DESC);
CREATE INDEX idx_auth_tokens_channel_type_expiry ON auth_tokens(channel, token_type, expires_at DESC);

-- Unique active token rule: one active token per user + type + channel.
-- Using unused-token rule keeps this simple and deterministic in SQL.
CREATE UNIQUE INDEX uq_auth_tokens_active_per_user_type_channel
    ON auth_tokens (user_id, token_type, COALESCE(channel, 'NONE'))
    WHERE is_used = FALSE;

